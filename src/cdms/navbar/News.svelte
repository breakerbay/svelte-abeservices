    <h2><strong>CDMS Features in ACT Engineering Excellence Awards</strong></h2>
    <p>In September 2005 the Canberra Division of Engineers Australia conducted the
        ACT Engineering Excellence Awards. The CDMS featured in the submission of NJ
        Construction Pty Ltd, which achieved a Highly Commended Award. NJ Construction
        used the CDMS to monitor the construction of the Murrarie 110kV transmission
        line through environmentally sensitive mangrove swamps. Helicopters were used
        to lay draw wires and then state of the art stringing machinery was used to
        complete the stringing of the transmission line. Further details can be found
        at <a href="http://www.ieaust.org.au/about_us/divisions/canberra/index.html ">http://www.ieaust.org.au/about_us/divisions/canberra/index.html </a>
    </p>
