<h2 align="center">CDMS Service</h2>

<blockquote>
    <p align="left">The CDMS offered by ABE Services is a Compliance Data Management Service utilising
        portable digital assistants (PDA's), the internet and a main computer database,
        to provide on-the-job compliance information and capture and collate construction
        data related to product or service compliance.</p>
    <p align="left">The scope of the CDMS ITP Service provides for planning and recording of compliance
        through Inspection and Test Plans, ITP's. It includes the following functionalities:</p>

    <ul>
        <li>Creation of ITP's on the CDMS, including by copying and customizing;</li>
        <li>Downloading of ITP's to Palm PDA's;</li>
        <li>Recording of compliance on Palm PDA's;</li>
        <li>Uploading of recorded compliance from Palm PDA's; and</li>
        <li>Viewing and printing reports from the CDMS.</li>
    </ul>

    <p align="left">The CDMS
        ITP Service is intended for use by contractors, subcontractors and others
        planning and recording the checking of work.</p>
    <p align="left">The CDMS ITP Service is also a valuable aid to contractors' and subcontractors'
        customers as it enables them to see records of checks undertaken, including
        when and by whom. There is no additional charge to those purchasing the
        CDMS ITP Service for providing their customers with on line access to view
        reports of checks, etc. Such access is of course, only granted with the
        permission of the party purchasing the CDMS ITP Service.</p>
