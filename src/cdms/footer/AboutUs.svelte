    <h2>About ABE Services</h2>

    <p><strong>ABE Services</strong> is  a privately owned Canberra based company formed in 2002 to develop
        and provide  a Compliance Data Management Service (<strong>CDMS</strong>) for building,
        construction and  related industries. ABE Services sees the CDMS
        as the way putting  into practice the sound management principles of Quality
        Assurance (QA) without the pain and paperwork it typically seems to involve
        in the building and construction industry.
    </p>
