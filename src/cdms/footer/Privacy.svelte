    <h4>Control of your Personal Information</h4>
    <p>When you register, or
        otherwise give us personal information, ABE Services will not share
        that information with third parties without your permission, other than for
        the limited exceptions already listed. ABE Services may send out periodic
        e-mails informing you of technical service or security issues related to the
        CDMS. You will not be able to choose to unsubscribe to these mailings, as
        they are considered an essential part of the service you have chosen.</p>
    <h4>Access to your Personal Information</h4>
    <p>We will provide you with
        the means to ensure that your personal information is correct and current.
        At any time you can review and edit your personal information by contacting
        ABE Services on abeservices@outlook.com.</p>
    <h4>Security of your Personal Information</h4>
    <p>ABE Services is
        committed to protecting the security of your personal information. We use
        a variety of security technologies and procedures to help protect your personal
        information from unauthorized access, use or disclosure. For example, we store
        the personal information you provide in computer servers supplied by tier
        1 ISP, with limited access and located in controlled facilities. When ABE
        Services Pty Ltd transmit sensitive information (such as a credit card number)
        over the internet, we protect it through the use of encryption, such as the
        Secure Socket Layer (SSL) protocol. Further more, all CDMS services are entirely
        SSL enabled.</p>
    <h4>Protection of Children's Personal Information</h4>
    <p>ABE Service&#8217;s web
        site and its CDMS are general audience sites and do not knowingly collect
        any personal information from children.</p>
    <h4>Use of Cookies</h4>
    <p>We use cookies within the secure area of our internet sites to ensure accurate information is provided
        to you while you conduct transactions online. A 'cookie' is a small data file
        that contains information in relation to your visit to a website. As soon
        as you exit the secure area of our internet sites the cookie that has been
        created is deleted. No information is stored on our cookies from one visit
        to the next.</p>
