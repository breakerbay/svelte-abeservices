    <p>A registered Checker is
        someone registered to use the CDMS on a personal digital assistant (PDA) such
        as a Palm Pilot. There is no charge for employees of the business entity who
        are not registered Checkers using the CDMS provided that, other than Guest
        Checkers, they are not involved in recording compliance information. There
        is no charge for Guest Checkers provided use by Guest Checkers, is only minor/occasional.</p>
    <p>Registered Checkers who
        are not an employee (permanent, casual or contract) of the business entity
        will be treated for charging purposes as belonging to a separate business
        entity.</p>
    <p>For the purposes of the
        above schedule, a month is a calendar month commencing with the first day
        of the month and ending with the last day of the month.</p>
    <p>A 15% reduction applies
        to the above rates when the service is purchased and paid for 12 months in
        advance, i.e. for Advance Purchase Accounts. Advance Purchase Accounts may
        be varied to increase the number of Checkers. In such cases payment for the
        variation for the remaining period is to be paid in advance and the reduced
        rate will apply. A reduction of the number of Checkers, either as originally
        subscribed or as varied, does not entitle the purchaser to any refund.</p>
