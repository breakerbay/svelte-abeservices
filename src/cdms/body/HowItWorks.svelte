        <h4><font size="2" face="Arial, Helvetica, sans-serif"><a name="A3"></a>Setting
            up ITP&#8217;s</font></h4>
        <p><font size="2" face="Arial, Helvetica, sans-serif">The process of developing
            ITP's has been simplified by the provision of industry standard ITP templates.
            These templates are provided to users either directly by ABE Services Pty
            Ltd, or by a relevant industry association to its members.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif"> These ITP templates
            can be copied into a user area and then into a project and used as is,
            or modified to suit the particular project. If needed the user can create
            ITP's from scratch. Because ITP's for a project are drawn from a list
            of templates developed/copied/modified by the user, then these user templates
            can be used over and over again from project to project. In fact entire
            projects can be copied to like projects to further speed up ITP preparation.<br>
            The setting up of projects and ITP's is undertaken by the user assigned
            the role of administrator.</font></p>
