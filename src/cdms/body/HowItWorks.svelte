        <h4>Using the CAS on Projects</h4>
        <p>The CAS manages the development and sign-off of checking of work on a <strong>Project. </strong>&nbsp;<strong>Inspection
                and Test Plans (ITP's)</strong> are the tool used to plan the checking
            of work (i.e. the inspection and test processes) for activities on a construction
            project.&nbsp; &nbsp;ITP's are the central plank of Quality Assurance on
            construction projects. </p>
        <p>Each <strong>Project</strong> has
            one or more <strong>ITP's</strong>covering the activities on the project,
            e.g. for bricklaying, roofing, etc.<strong>&nbsp; </strong>In the CAS an <strong>ITP</strong> contains
            a number of steps that define the process for doing the work (called <strong>Process
                Steps</strong>) and the points at which checks (verification) are carried
            out (called <strong>Verification Points</strong>). </p>
        <p><strong>Process Steps </strong>are
            the steps taken to deliver the work. <strong>Verification Points</strong> define
            the checks to be done at specific points in the work process to confirm/demonstrate
            compliance with specified requirements. &nbsp;On the CAS web site both <strong>Process
                Steps</strong> and <strong>Verification Points</strong> are visible but
            only <strong>Verification Points </strong>are downloaded onto the PDA (Palm).</p>
        <p>        <strong>Verification Points </strong>have
            one or more <strong>Criteria</strong> setting
            out in detail what specified requirements must be met.</p>
        <p>A <strong>Verification
            Point </strong>may have some explanation attached to it, defined in a<strong> Details </strong>section,
            explaining the relevance of the <strong>Verification Point, </strong>&nbsp;such
            as references to relevant industry standards.</p>
        <p>Additionally, there is
            the provision on the PDA to record <strong>Follow up Actions</strong> when
            a <strong>Verification Point</strong> can't be ticked off the first time
            it is checked. &nbsp;(In the Quality Assurance Standard ISO 9001:2000 these
            are termed a non-conformance. The CAS provides a very easy way to record
            these.&nbsp; This helps control work and provides valuable feedback to
            identify improvements.)</p>
        <p><strong>ITP's</strong> are
            assigned <strong>Work Lots.</strong> A <strong>Work Lot</strong> defines
            the work that is covered by the check being undertaken.&nbsp; As an example
            an <strong>ITP (</strong>for say &quot;Roof Cladding&quot;) may have assigned
            to it two or more <strong>Work Lots, </strong>e.g. for the East side of the
            building and for the West side of the building.</p>
