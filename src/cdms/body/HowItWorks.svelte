        <h4>Getting started</h4>
        <p>To get started a user
            first needs to sign up with ABE Services Pty Ltd. Once signed up the user
            will be provided with a password to access the CAS. The initial user will
            be given access as an administrator. As an administrator the user will
            be able to assign any other licence holders as an administrator or a checker.</p>
        <p> An administrator has
            the ability to establish projects, create/modify ITP's and associated work
            lots, and assigns specific ITP's to individual Checkers. A Checker, on
            the other hand can only complete the activities viewed on his/her allocated
            PDA, as well being able to download and upload data to the PDA.</p>
<!--        <p><a href="#/how" target="_top">Return to top of page</a></p>-->
        <h4>Setting up ITP&#8217;s</h4>
        <p>The process of developing
            ITP's has been simplified by the provision of industry standard ITP templates.
            These templates are provided to users either directly by ABE Services Pty
            Ltd, or by a relevant industry association to its members.</p>
        <p> These ITP templates
            can be copied into a user area and then into a project and used as is,
            or modified to suit the particular project. If needed the user can create
            ITP's from scratch. Because ITP's for a project are drawn from a list
            of templates developed/copied/modified by the user, then these user templates
            can be used over and over again from project to project. In fact entire
            projects can be copied to like projects to further speed up ITP preparation.<br>
            The setting up of projects and ITP's is undertaken by the user assigned
            the role of administrator.</p>
<!--        <p><a href="#/how" target="_top">Return to top of page</a></p>-->
        <h4>Recording data</h4>
        <p>When ready to undertake
            an inspection to collect compliance data, the Checker would synchronise
            his/her PDA with the remote database to download the compliance/inspection
            documentation to the PDA.</p>
        <p> The user then takes
            the PDA &#8216;into the field&#8217; and completes the
            activities required by the compliance/inspection planning documentation
            loaded  onto the PDA, by recording data into the mobile device as required
            by the downloaded  documentation. </p>
        <p> The data recorded either
            confirms compliance with requirements, or where compliance cannot be achieved,
            identifies a follow-up action to be undertaken before a subsequent inspection
            is carried out. The action also identifies if there are client requirements
            to be addressed. </p>
        <p> On completion of inspection
            activities the user synchronises the PDA with the database, uploading the
            data collected.</p>
        <p> The uploaded data populates
            the compliance/inspection documentation to provide a record of compliance
            actions.</p>
        <p> All activity on the PDA is date stamped.</p>
<!--        <p><a href="#/how" target="_top">Return to top of page</a></p>-->
        <h4>Viewing reports</h4>
        <p>The user can review ITP's
            and associated compliance data in an appropriate reporting format (onscreen
            or printed reports). Reports available cover the ITP, the ITP with collected
            data (the name of the checker, the data and time when each activity was signed
            off, work lots used and non-conformance reports. </p>
