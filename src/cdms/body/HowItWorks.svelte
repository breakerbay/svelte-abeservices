        <h4><font size="2" face="Arial, Helvetica, sans-serif"><a name="A2"></a>Getting
            started</font></h4>
        <p><font size="2" face="Arial, Helvetica, sans-serif">To get started a user
            first needs to sign up with ABE Services Pty Ltd. Once signed up the user
            will be provided with a password to access the CDMS. The initial user will
            be given access as an administrator. As an administrator the user will
            be able to assign any other licence holders as an administrator or a checker.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif"> An administrator has
            the ability to establish projects, create/modify ITP's and associated work
            lots, and assigns specific ITP's to individual Checkers. A Checker, on
            the other hand can only complete the activities viewed on his/her allocated
            PDA, as well being able to download and upload data to the PDA.</font></p>
