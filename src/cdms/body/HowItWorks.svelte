        <h4><font size="2" face="Arial, Helvetica, sans-serif"><a name="Capacity"></a>Palm
            Capacity Limitation</font></h4>
        <p><font size="2" face="Arial, Helvetica, sans-serif">Attention of users and potential users of the CDMS is drawn to
            limitations on the size and number of projects that may be downloaded
            to PDAs (Palms). As these limits vary depending on the PDA used and
            the project(s) involved it is not possible to provide precise
            guidance on the limits that may be encountered. When the download
            capacity is exceeded not all the project information is downloaded to
            the PDA.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif">To date projects with of the order of 25 Inspection and Test Plans
            and 10 worklots have been successfully downloaded. Projects with
            significantly more worklots but fewer ITPs have also been
            successfully handled. More than ten projects have also been
            successfully downloaded to a single Palm.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif">The download capacity
            may be adversely affected if the free space on  the PDA (Palm) is less
            than 5 Mb and there is a significant skew in  the ITP to Work Lot ratio,
            ie a high number of Work Lots to the number  of ITPs or vice versa.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif">The download capacity of the CDMS indicated above has been sufficient
            for trial projects undertaken in 2004. These projects were for
            building, civil and landscape works up to the order of $2,000,000.</font></p>
        <p><font size="2" face="Arial, Helvetica, sans-serif">Users looking at employing the CDMS on larger projects where capacity
            limitations may occur are invited to contact ABE Services Pty Ltd to
            discuss their particular needs.</font></p>
