        <h4>Providing access to others</h4>
        <p>If the administrator
            wishes to, the administrator can grant to external parties an  on line
            view of the ITP's and other reports. This allows project compliance information
            to be shared with others (clients, superintendents, project managers) in
            real time. These external users may only view data. They cannot alter data.</p>

